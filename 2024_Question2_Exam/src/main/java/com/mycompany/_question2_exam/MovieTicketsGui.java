    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // Method for the process option 
        try{
            // Getting data from user
            String movie = jComboBox1.getSelectedItem().toString();
            int tickets = Integer.parseInt(jTextField1.getText());
            double price = Double.parseDouble(jTextField2.getText());
            
            // Create an instance of the MovieTicket
            MovieTicketData data = new MovieTicketData(movie, tickets, price);
            
            // Validating user Input 
            if (!movieTickets.ValidateData(data)) {
                JOptionPane.showMessageDialog(null, "Invaild input.Please check your entries", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Calculate total including VAT
            double total = movieTickets.CalculateTotalTicketPrice(tickets, price);
            
            // Generating the report 
            String report = 
                    
                      "MOVIE NAME: " + movie + "\n"
                    + "MOVIE TICKET PRICE: R " + price + "\n"
                    + "NUMBER OF TICKETS: " + tickets + "\n"
                    + "TOTAL TICKET PRICE: R " + total + "\n";
            
            // Display report in the text area 
            jTextArea1.setText(report);
            
            // Saving to the file
            saveReportToFile(report);
            
            JOptionPane.showMessageDialog(null, "Report generated and saved successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

                    
        }catch (NumberFormatException  ex){
            JOptionPane.showMessageDialog(null, "Please enter valid numeric values for tickets and price.", "Error", JOptionPane.ERROR_MESSAGE);
        }catch (Exception  ex){
            JOptionPane.showMessageDialog(null, "Error processing ticket: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

