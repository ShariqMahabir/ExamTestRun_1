    // Method to save the report to a text file named report.txt
    private void saveReportToFile(String report) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("report.txt", true))) {
            writer.write(report);
            writer.write("\n------------------------------\n");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error saving report to file.", "File Error", JOptionPane.ERROR_MESSAGE);
        }
    }

